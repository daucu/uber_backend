const mongoose = require('mongoose');
const users_schema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    confirm_password: {
        type: String,
        required: true
    },
}, { timestamps: true });

module.exports = mongoose.model('users', users_schema);